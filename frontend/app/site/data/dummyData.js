// Helper function to get random entry from dummy data
export const getRandomEntry = (dataArray) => {
  if (!Array.isArray(dataArray) || dataArray.length === 0) return null;
  return dataArray[Math.floor(Math.random() * dataArray.length)];
};

// Helper function to generate dummy data with timestamps
export const generateDummyTimestamp = (daysAgo = 0) => {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  return date.toISOString().split("T")[0];
};
